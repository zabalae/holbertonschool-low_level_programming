/**
 * reverse_array - function that reverses the content of an array of integers
 *
 * @a: array
 *
 * @n: number of elements of the array
 *
 * Return: void
 */

void reverse_array(int *a, int n)
{
	int *x;

	int i, y;

	y = n;

	for (i = 0; i <= n; i++)
	{
		x[i] - a[y];
		y--;
	}

	x = 0;

	y = 0;

	while (i > 0 - 1)
	{
		a[x] = y[x];
		x++;
		y++;
	}
}
